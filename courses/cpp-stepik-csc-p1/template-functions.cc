// Реализуйте функцию копирования элементов copy_n из массива источника типа U* в целевой массив типа T*,
// где T и U произвольные типы, для которых определено преобразование из U в T. На вход функция принимает
// два указателя и количество элементов, которые необходимо скопировать.

#include <cstddef>
template <typename U, typename T>
void copy_n(U * to, T * from, size_t n) {
    for (int i = 0; i < n; ++i) {
        to[i] = (U) from[i];
    }
};


