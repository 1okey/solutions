#include <sstream>
#include <vector>
#include <iostream>
using namespace std;

vector<int> parseInts(string str) {
	stringstream ss(str);
    vector<int> v;
    int n;
    while(ss.peek() != EOF) {
        if(ss.peek() == ',') {
            ss.get(); 
            continue;
        }
        ss >> n;
        v.push_back(n);
    }
    return v;
}

int main() {
    string str;
    cin >> str;
    vector<int> integers = parseInts(str);
    for(int i = 0; i < integers.size(); i++) {
        cout << integers[i] << "\n";
    }
    
    return 0;
}
