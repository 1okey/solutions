#include <iostream>

// Дано два целых числа A и B (A <= B, A >= 1, B <= 30000). Выведите через пробел все чётные
//числа от A до B (включительно). Для проверки целого числа x на чётность используется операция
// взятия остатка от деления на 2,  которая в C++ оформляется с помощью символа "%".

int main() {
    int a, b;
    std::cin >> a >> b;
    while (a <= b) {
        if (a % 2 == 0) {
            std::cout << a << ' ';
        }
        a++;
    }
    return 0;
}

